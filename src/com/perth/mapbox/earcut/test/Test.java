	public static boolean isSameContent(int[] a, int[] b) {
		boolean isSame = true;
		if(a.length != b.length) {
			isSame = false;
		}
		else {
			for(int i=0;i<a.length;i++) {
				if(a[i] != b[i]) {
					isSame = false;
					break;
				}
			}
		}
		
		return isSame;
	}
	
